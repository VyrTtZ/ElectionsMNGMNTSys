package electionmngmntsys.mhashmap;
//----------------------------------------------------------------------------------------------------------------------
public class mNodeH <X, Y>{
    X key;
    Y value;
    //----------------------------------------------------------------------------------------------------------------------
    public mNodeH(X k, Y v){
        key = k;
        value = v;

    }
    //----------------------------------------------------------------------------------------------------------------------
    public X getKey() {
        return key;
    }
    //----------------------------------------------------------------------------------------------------------------------
    public void setKey(X key) {
        this.key = key;
    }
    //----------------------------------------------------------------------------------------------------------------------
    public Y getValue() {
        return value;
    }
    //----------------------------------------------------------------------------------------------------------------------
    public void setValue(Y value) {
        this.value = value;
    }
    //----------------------------------------------------------------------------------------------------------------------
}
